        public Color? CustomFontColorForContentType(ContentType contentType) {

            switch (contentType) {
                case ContentType.Keywords:
                    return Color.FromArgb(0xFF, 0x00, 0x00, 0xFF);
                case ContentType.Attributes:
                    return Color.FromArgb(0xFF, 0xFF, 0x00, 0xFF);
                case ContentType.Numbers:
                    return Color.FromArgb(0xFF, 0x33, 0x99, 0xCC);
            }

            return null;
